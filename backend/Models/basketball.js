import mongoose from "mongoose";

const sportSchema = new mongoose.Schema({
  id: {
    type: Number,
    required: true,
    unique: true
  },
  name: {
    type: String,
    required: true
  },
  text: {
    type: String,
    required: true
  },
  image: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  }
}, { minimize: false });

const sportModel3 =  mongoose.model('basketball', sportSchema);

export default sportModel3;
